   public khachHang getKhachHang() {

        if (txtIDKH.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn không được để trống id khach hang");
            return null;
        }
        if (txtTenKH.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn không được để trống ten khach hang");
            return null;
        }
        if (txtSDT.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn không được để trống so dien thoai");
            return null;
        }
        if (txtDiaChi.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn không được để trống dia chi");
            return null;
        }
        if (txtEmail.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn không được để trống email");
            return null;
        }

        khachHang kh = new khachHang();
        kh.setIDKHACHHANG(Integer.parseInt(txtIDKH.getText()));
        kh.setTENKHACHHANG(txtTenKH.getText());
        kh.setSODIENTHOAI(txtSDT.getText());
        kh.setDIACHI(txtDiaChi.getText());
        kh.setEMAIL(txtEmail.getText());
        kh.setNGAYSINH(txtNgaySinh.getDate());
        int gt;
        if (rbnNu.isSelected()) {
            gt = 0;
        }else{
            gt = 1;
        }
        int tt;
        if (rbnHoatDong.isSelected()) {
            tt = 1;
        }else{
            tt = 0;
        }
        kh.setTRANGTHAI(tt);
        kh.setGIOITINH(gt);
        return kh;
    }


