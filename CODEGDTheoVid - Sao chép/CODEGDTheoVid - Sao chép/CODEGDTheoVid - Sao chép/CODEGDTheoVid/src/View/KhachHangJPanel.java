  public void fillToForm(int index) {
        khachHang kh = listKH.get(index);
        txtIDKH.setText(String.valueOf(kh.getIDKHACHHANG()));
        txtTenKH.setText(kh.getTENKHACHHANG());
        txtSDT.setText(kh.getSODIENTHOAI());
        txtDiaChi.setText(kh.getDIACHI());
        txtEmail.setText(kh.getEMAIL());
        txtNgaySinh.setDate(kh.getNGAYSINH());
        if (kh.getGIOITINH() == 1) {
            rbnNam.setSelected(true);
        } else {
            rbnNu.setSelected(true);

        }
        if (kh.getTRANGTHAI() == 1) {
            rbnHoatDong.setSelected(true);
        } else {
            rbnKhongHoatDong.setSelected(true);
        }

    }

    public void fillToTable(List<khachHang> list) {
        DefaultTableModel tblModel = new DefaultTableModel();
        String cols[] = {"ID KH", "Họ Tên", "SDT", "Địa chỉ", "Email", "Ngày Sinh", "Giới Tính", "Ngày Tạo", "Ngày Sửa", "Trạng Thái"};
        tblModel.setColumnIdentifiers(cols);
        tblModel.setRowCount(0);
        for (khachHang kh : list) {
            String gt = "";
            if (kh.getGIOITINH() == 0) {
                gt = "Nữ";
            } else {
                gt = "Nam";
            }
            String tt = "";
            if (kh.getTRANGTHAI() == 0) {
                tt = "Không Hoạt Động";
            } else {
                tt = "Hoạt Động";
            }
            tblModel.addRow(new Object[]{
                kh.getIDKHACHHANG(),
                kh.getTENKHACHHANG(),
                kh.getSODIENTHOAI(),
                kh.getDIACHI(),
                kh.getEMAIL(),
                kh.getNGAYSINH(),
                kh.getGIOITINH(),
                kh.getNGAYTAO(),
                kh.getNGAYSUA(),
                kh.getTRANGTHAI()
            });
        }
        tblKhachHang.setModel(tblModel);
    }

